#!/usr/bin/env bash
# =============================================================================
# Refresh GitHub App token and write static exports to ~/.github_env
# Called by systemd timer (github-app-auth-refresh.timer)
# =============================================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$(readlink -f "${BASH_SOURCE[0]}")")" && pwd)"

# Load credentials from ~/.env
set -a
source ~/.env
set +a

# Generate fresh token
source "$SCRIPT_DIR/github-token.sh"

# Write static exports for scripts that need token without env inheritance
cat > ~/.github_env <<EOF
# Auto-generated by github-app-auth
# Updated: $(date -Iseconds)
export GITHUB_TOKEN="$GITHUB_TOKEN"
export GITHUB_INSTALLATION_ID="$GITHUB_INSTALLATION_ID"
export GITHUB_JWT="$GITHUB_JWT"
EOF

chmod 600 ~/.github_env
echo "GitHub token refreshed at $(date -Iseconds)" >&2
